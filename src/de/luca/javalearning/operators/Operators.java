package de.luca.javalearning.operators;

public class Operators {

    public static void main(String[] args) {
        //Arithmetische Operatoren
        int zahl1 = 1;
        int zahl2 = 2;
        int hauraus;

        hauraus = zahl1 % zahl2;

        System.out.println(hauraus);

        hauraus = zahl1 * zahl2;

        System.out.println(hauraus);

    }

}
